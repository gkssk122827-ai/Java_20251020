package ex08;

public class Printer {
	
	public void pintln(int value) {
		System.out.println(value);
	}
	public void pintln(Boolean value) {
		System.out.println(value);
	}
	public void pintln(double value) {
		System.out.println(value);
	}
	public void pintln(String value) {
		System.out.println(value);
	}
	

}
